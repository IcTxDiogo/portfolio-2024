export type PokemonTypes = keyof typeof pokemonTypesWithBoost;
export type BoostType = keyof (typeof pokemonTypesWithBoost)["normal"]["boost"];

export function getTotalBoost(
  start: BoostType,
  end: BoostType,
  type: PokemonTypes,
) {
  let result = 0;
  for (let i = start; i <= end; i++) {
    result += pokemonTypesWithBoost[type].boost[i];
  }
  return result;
}

